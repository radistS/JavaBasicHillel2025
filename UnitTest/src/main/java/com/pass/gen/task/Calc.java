package com.pass.gen.task;

public class Calc {
    public int add(int a, int b) {
        return a + b;
    }

    public double div(int a, int b) {
        return a / b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int mult(int a, int b) {
        return a * b;
    }
}
